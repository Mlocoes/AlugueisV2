/**
 * MÃ³dulo Dashboard - GestÃ£o do dashboard principal
 * Exibe estatÃ­sticas, grÃ¡ficos e resumo do sistema
 */

class DashboardModule {
    // Log de definiciÃ³n de clase
    static logDefinition() {
        console.log('âœ… DashboardModule definido en window');
    }
    constructor() {
        this.charts = {};
        this.data = {};
    }

    /**
     * Carregar dados do dashboard
     */
    async load() {
        // Timeout de seguranÃ§a para evitar carregamento infinito
        const loadingTimeout = setTimeout(() => {
            window.uiManager?.hideLoading();
        }, 10000); // 10 segundos no mÃ¡ximo

        try {
            window.uiManager?.showLoading('Carregando dashboard...');

            // Carregar dados em paralelo
            const [propietarios, imoveis, alugueis] = await Promise.all([
                window.apiService.getProprietarios(),
                window.apiService.getImoveis(),
                window.apiService.getAlugueis()
            ]);

            // Los mÃ©todos del apiService ya devuelven los datos directamente
            this.data = { 
                propietarios: propietarios || [], 
                imoveis: imoveis || [], 
                alugueis: alugueis || [] 
            };

            console.log('ðŸ“Š Dados carregados no dashboard:', {
                propietarios: this.data.propietarios.length,
                imoveis: this.data.imoveis.length,
                alugueis: this.data.alugueis.length
            });

            // Atualizar estatÃ­sticas primeiro
            this.updateStats();

            // Esperar um pouco para garantir que o DOM esteja pronto
            await new Promise(resolve => setTimeout(resolve, 100));

            // Criar grÃ¡ficos depois que os dados estiverem prontos
            await this.createCharts();

        } catch (error) {
            // ...cÃ³digo existente...
            window.uiManager?.showAlert('Erro ao carregar dados do dashboard: ' + error.message, 'error');
        } finally {
            clearTimeout(loadingTimeout);
            window.uiManager?.hideLoading();
        }
    }

    /**
     * Atualizar estatÃ­sticas do dashboard
     */
    updateStats() {
        const { propietarios = [], imoveis = [], alugueis = [] } = this.data;

        // ...cÃ³digo existente...

        // Atualizar contadores
        this.updateCounter('dashboard-total-proprietarios', propietarios.length);
        this.updateCounter('dashboard-total-inmuebles', imoveis.length);

        // Novo: valor financeiro total dos aluguÃ©is do ano corrente
        const totalAlugueisAno = this.valorFinanceiroAlugueisAnoCorrente(alugueis);
        this.updateCounter('dashboard-alugueis-ano-corrente', `R$ ${totalAlugueisAno.toLocaleString('pt-BR', { minimumFractionDigits: 2 })}`);

        // Calcular receitas mensais
        const receitas = this.calculateMonthlyIncome(alugueis);
        this.updateCounter('dashboard-ingresos-mensuales', `R$ ${receitas.toLocaleString('pt-BR', { minimumFractionDigits: 2 })}`);

        // ...cÃ³digo existente...
    }

    /**
     * Soma o valor financeiro total dos aluguÃ©is do maior ano disponÃ­vel nos dados
     */
    valorFinanceiroAlugueisAnoCorrente(alugueis) {
        if (!alugueis || alugueis.length === 0) return 0;
        // Encontrar o maior ano disponÃ­vel
        let maxAno = Math.max(...alugueis.map(a => a.ano || 0));
        return alugueis
            .filter(a => a.ano === maxAno)
            .reduce((total, a) => total + (a.valor_liquido_proprietario || 0), 0);
    }

    /**
     * Atualizar contador individual
     */
    updateCounter(elementId, value) {
        const element = document.getElementById(elementId);
        if (element) {
            element.textContent = value;
        }
    }

    /**
     * Calcular receitas do Ãºltimo mÃªs disponÃ­vel nos dados
     */
    calculateMonthlyIncome(alugueis) {
        if (!alugueis || alugueis.length === 0) return 0;
        // Encontrar o maior ano e mÃªs disponÃ­vel
        let maxAno = Math.max(...alugueis.map(a => a.ano || 0));
        let mesesDoAno = alugueis.filter(a => a.ano === maxAno).map(a => a.mes || 0);
        let maxMes = Math.max(...mesesDoAno);
        // Somar todos os valores desse mÃªs/ano
        return alugueis.reduce((total, aluguel) => {
            if (aluguel.ano === maxAno && aluguel.mes === maxMes) {
                return total + (aluguel.valor_liquido_proprietario || 0);
            }
            return total;
        }, 0);
    }

    /**
     * Criar grÃ¡ficos do dashboard
     */
    async createCharts() {
        try {
            // Verificar se os canvas existem antes de criar os grÃ¡ficos
            const incomeCanvas = document.getElementById('ingresosChart');
            if (!incomeCanvas) {
                // ...cÃ³digo existente...
                return;
            }
            // ...cÃ³digo existente...
            // Primeiro destruÃ­mos todos os grÃ¡ficos existentes
            this.destroyAllCharts();
            // Depois criamos os novos
            this.createIncomeChart();
            //this.createDistributionChart();
            // ...cÃ³digo existente...
        } catch (error) {
            // ...cÃ³digo existente...
            // NÃ£o lanÃ§ar o erro para nÃ£o bloquear o resto do dashboard
        }
    }

    /**
     * Destruir todos os grÃ¡ficos existentes
     */
    destroyAllCharts() {
        try {
            if (this.charts.income) {
                this.charts.income.destroy();
                this.charts.income = null;
            }

            // ...cÃ³digo existente...
        } catch (error) {
            // ...cÃ³digo existente...
        }
    }    /**
     * Criar grÃ¡fico de receitas
     */
    createIncomeChart() {
        let canvas = document.getElementById('ingresosChart');
        if (!canvas) {
            // ...cÃ³digo existente...
            return;
        }

        try {
            // Destruir grÃ¡fico anterior se existir
            if (this.charts.income) {
                this.charts.income.destroy();
                this.charts.income = null;
            }
            // Remover e recriar o canvas para garantir que Chart.js nÃ£o reutilize instÃ¢ncias antigas
            const parent = canvas.parentNode;
            parent.removeChild(canvas);
            canvas = document.createElement('canvas');
            canvas.id = 'ingresosChart';
            parent.appendChild(canvas);

            const chartData = this.processIncomeData();
            if (!chartData || !chartData.labels || !chartData.values) {
                // ...cÃ³digo existente...
                return;
            }

            const ctx = canvas.getContext('2d');
            if (!ctx) {
                // ...cÃ³digo existente...
                return;
            }

            // ...cÃ³digo existente...

            this.charts.income = new Chart(ctx, {
                type: 'line',
                data: {
                    labels: chartData.labels,
                    datasets: [{
                        label: 'Receitas (R$)',
                        data: chartData.values,
                        borderColor: '#36A2EB',
                        backgroundColor: 'rgba(54, 162, 235, 0.1)',
                        borderWidth: 3,
                        fill: true,
                        tension: 0.4,
                        pointBackgroundColor: '#36A2EB',
                        pointBorderColor: '#ffffff',
                        pointBorderWidth: 2,
                        pointRadius: 6,
                        pointHoverRadius: 8
                    }]
                },
                options: {
                    responsive: true,
                    maintainAspectRatio: false,
                    plugins: {
                        legend: {
                            display: false
                        },
                        tooltip: {
                            backgroundColor: 'rgba(0, 0, 0, 0.8)',
                            titleColor: 'white',
                            bodyColor: 'white',
                            borderColor: '#36A2EB',
                            borderWidth: 1
                        }
                    },
                    scales: {
                        x: {
                            grid: {
                                display: false
                            }
                        },
                        y: {
                            beginAtZero: true,
                            grid: {
                                borderDash: [5, 5]
                            },
                            ticks: {
                                callback: function (value) {
                                    return 'R$' + value.toLocaleString();
                                }
                            }
                        }
                    }
                }
            });

        } catch (error) {
            // ...cÃ³digo existente...
            // ...cÃ³digo existente...
        }
    }

    /**
     * Criar grÃ¡fico de distribuiÃ§Ã£o
     */

    /**
     * Processar dados para grÃ¡fico de receitas
     */
    processIncomeData() {
        const { alugueis = [] } = this.data;
        if (!alugueis.length) {
            return { labels: [], values: [] };
        }
        // Agrupar receitas por ano-mÃªs
        const monthlyIncome = {};
        alugueis.forEach(aluguel => {
            if (aluguel.valor_liquido_proprietario && aluguel.mes && aluguel.ano) {
                const monthKey = `${aluguel.ano}-${aluguel.mes.toString().padStart(2, '0')}`;
                if (!monthlyIncome[monthKey]) monthlyIncome[monthKey] = 0;
                monthlyIncome[monthKey] += aluguel.valor_liquido_proprietario;
            }
        });
        // Ordenar as chaves ano-mÃªs
        const sortedKeys = Object.keys(monthlyIncome).sort((a, b) => {
            const [aAno, aMes] = a.split('-').map(Number);
            const [bAno, bMes] = b.split('-').map(Number);
            return aAno !== bAno ? aAno - bAno : aMes - bMes;
        });
        return {
            labels: sortedKeys.map(key => {
                const [year, month] = key.split('-');
                const date = new Date(year, month - 1);
                return date.toLocaleDateString('pt-BR', { month: 'short', year: 'numeric' });
            }),
            values: sortedKeys.map(key => monthlyIncome[key])
        };
    }


    /**
     * Atualizar o dashboard
     */
    async refresh() {
        await this.load();
    }
}

